const RollingGallery = ({
  autoplay = false,
  pauseOnHover = false,
  images = [],
}) => {
  images = images.length > 0 ? images : IMGS;
  images = images.slice(0, 10); // show only 10 cards

  const [isScreenSizeSm, setIsScreenSizeSm] = useState(
    window.innerWidth <= 640
  );
  useEffect(() => {
    const handleResize = () => setIsScreenSizeSm(window.innerWidth <= 640);
    window.addEventListener("resize", handleResize);
    return () => window.removeEventListener("resize", handleResize);
  }, []);

  // Show 4-5 cards at a time, fill the screen
  const visibleCards = isScreenSizeSm ? 4 : 5;
  const cardWidth = isScreenSizeSm ? Math.floor(window.innerWidth / 4.5) : Math.floor(window.innerWidth / 5.5);
  const cardHeight = Math.floor(cardWidth * 1.4);
  const gap = 8; // px, small gap
  const cylinderWidth = visibleCards * (cardWidth + gap);
  const faceCount = images.length;
  const faceWidth = cardWidth;
  const radius = cylinderWidth / (0.8 * Math.PI);

  const dragFactor = -0.05;
  const rotation = useMotionValue(0);
  const controls = useAnimation();

  const transform = useTransform(
    rotation,
    (val) => `rotate3d(0,1,0,${val}deg)`
  );

  const startInfiniteSpin = (startAngle) => {
    controls.start({
      rotateY: [startAngle, startAngle - 360],
      transition: {
        duration: 20,
        ease: "linear",
        repeat: Infinity,
      },
    });
  };

  useEffect(() => {
    // No autoplay
    controls.stop();
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  const handleUpdate = (latest) => {
    if (typeof latest.rotateY === "number") {
      rotation.set(latest.rotateY);
    }
  };

  const handleDrag = (_, info) => {
    controls.stop();
    let newRotation = rotation.get() + info.offset.x * dragFactor;
    // Keep rotation between 0 and 360
    newRotation = ((newRotation % 360) + 360) % 360;
    rotation.set(newRotation);
  };

  const handleDragEnd = (_, info) => {
    let finalAngle = rotation.get() + info.velocity.x * dragFactor;
    finalAngle = ((finalAngle % 360) + 360) % 360;
    rotation.set(finalAngle);

    if (autoplay) {
      startInfiniteSpin(finalAngle);
    }
  };

  const handleMouseEnter = () => {
    if (autoplay && pauseOnHover) {
      controls.stop();
    }
  };
  const handleMouseLeave = () => {
    if (autoplay && pauseOnHover) {
      const currentAngle = rotation.get();
      startInfiniteSpin(currentAngle);
    }
  };

  return (
    <div className="relative h-[500px] w-full overflow-hidden">
      <div
        className="absolute top-0 left-0 h-full w-[48px] z-10"
        style={{
          background:
            "linear-gradient(to left, rgba(0,0,0,0) 0%, #060010 100%)",
        }}
      />
      <div
        className="absolute top-0 right-0 h-full w-[48px] z-10"
        style={{
          background:
            "linear-gradient(to right, rgba(0,0,0,0) 0%, #060010 100%)",
        }}
      />

      <div className="flex h-full items-center justify-center [perspective:1000px] [transform-style:preserve-3d]">
        <motion.div
          drag="x"
          dragElastic={0}
          onDrag={handleDrag}
          onDragEnd={handleDragEnd}
          onMouseEnter={handleMouseEnter}
          onMouseLeave={handleMouseLeave}
          animate={controls}
          onUpdate={handleUpdate}
          style={{
            transform: transform,
            rotateY: rotation,
            width: cylinderWidth,
            transformStyle: "preserve-3d",
          }}
          className="flex min-h-[200px] cursor-grab items-center justify-center [transform-style:preserve-3d]"
        >
          {images.map((url, i) => (
            <div
              key={i}
              className="group absolute flex h-fit items-center justify-center [backface-visibility:hidden]"
              style={{
                width: `${faceWidth}px`,
                transform: `rotateY(${(360 / faceCount) * i}deg) translateZ(${radius}px)`
              }}
            >
              <div
                className="bg-white/10 rounded-2xl shadow-xl border-2 border-white/30 overflow-hidden flex flex-col items-center justify-center transition-transform duration-300 ease-out group-hover:scale-105"
                style={{ width: cardWidth, height: cardHeight, margin: `0 ${gap/2}px` }}
              >
                <img
                  src={url}
                  alt="gallery"
                  className="object-cover w-full h-full"
                  style={{ aspectRatio: '3/4' }}
                />
              </div>
            </div>
          ))}
        </motion.div>
      </div>
    </div>
  );
};

export default RollingGallery; 